$(document).ready(function() {
    $("#block_1").hover(
        function() {
            $(this).find(".text_menu").fadeOut(250); 
            $(this).find(".text_menu2").fadeIn(250); 
           
        },
        function() {
            $(this).find(".text_menu2").fadeOut(250); 
            $(this).find(".text_menu").fadeIn(250); 
        }    
    );
});


